async function run() {
    let deptsMap = {};
    const { data: dData } = await supabase.from('departments').select('id, name');
    if (dData) {
        dData.forEach(d => deptsMap[d.name.trim().toUpperCase()] = d.id);
    }

    let records = [];

    const lines = ocr.split('\n');
    for (let line of lines) {
        line = line.trim();
        if (!line) continue;
        const match = line.match(/^(.*?)\s+(C-\d{4})\s+(.*?)\s+(\d{2}\/\d{2}\/\d{4})\s+(\S+)\s+(.*)$/);
        if (match) {
            let nome_cargo = match[1].trim();
            let pc_code = match[2];
            let plano_carreira = match[3].trim();
            let admission = match[4];
            let centro_custo = match[5].trim();
            let departamento = match[6].trim();

            let cargo = "";
            let nome = nome_cargo;
            for (let c of cargos) {
                if (nome_cargo.endsWith(c)) {
                    cargo = c;
                    nome = nome_cargo.substring(0, nome_cargo.length - c.length).trim();
                    break;
                }
            }
            if (!cargo) {
                let parts = nome_cargo.split(' ');
                cargo = parts.pop();
                nome = parts.join(' ');
            }

            let admParts = admission.split('/');
            let adm_date = `${admParts[2]}-${admParts[1]}-${admParts[0]}`;

            let deptId = deptsMap[departamento.toUpperCase()];
            if (!deptId) {
                const { data: newD } = await supabase.from('departments').insert({ name: departamento }).select('id').single();
                if (newD) {
                    deptId = newD.id;
                    deptsMap[departamento.toUpperCase()] = deptId;
                }
            }

            records.push({
                name: nome,
                role: cargo,
                admission_date: adm_date,
                department_id: deptId,
                cost_center: centro_custo,
                status: 'Ativo',
                profile_code: pc_code,
                level: plano_carreira
            });
        }
    }

    console.log(`Parsed ${records.length} records. Upserting into employees...`);

    let newCount = 0;
    let updCount = 0;
    for (let r of records) {
        // Tenta incluir todos os campos (requer que a migration tenha rodado)
        let recordToInsert = { ...r };
        
        const { data: exist } = await supabase.from('employees').select('id').eq('name', r.name).maybeSingle();
        if (exist) {
            let { error } = await supabase.from('employees').update(recordToInsert).eq('id', exist.id);
            if (error && error.code === 'PGRST204') {
                // Colunas inexistentes (não rodou migration). Remove profile_code e level.
                delete recordToInsert.profile_code;
                delete recordToInsert.level;
                let fallback = await supabase.from('employees').update(recordToInsert).eq('id', exist.id);
                if (fallback.error) console.error("Error updating fallback", r.name, fallback.error);
                else updCount++;
            } else if (error) {
                console.error("Error updating", r.name, error);
            } else {
                updCount++;
            }
        } else {
            let { error } = await supabase.from('employees').insert(recordToInsert);
            if (error && error.code === 'PGRST204') {
                delete recordToInsert.profile_code;
                delete recordToInsert.level;
                let fallback = await supabase.from('employees').insert(recordToInsert);
                if (fallback.error) console.error("Error inserting fallback", r.name, fallback.error);
                else newCount++;
            } else if (error) {
                console.error("Error inserting", r.name, error);
            } else {
                newCount++;
            }
        }
    }
    console.log(`Done. Updated ${updCount}, Inserted ${newCount}.`);
}

run();
