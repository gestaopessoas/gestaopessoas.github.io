def get_class(percentile):
    if percentile >= 99: return 'Muito Superior'
    if percentile >= 95: return 'Superior'
    if percentile >= 75: return 'Médio Superior'
    if percentile >= 30: return 'Médio'
    if percentile >= 10: return 'Médio Inferior'
    return 'Inferior'

