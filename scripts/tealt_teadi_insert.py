sql_statements.append("-- TEALT MIGRATION")

for table in data:
    for cat_idx, category in enumerate(table['categories']):
        for row_idx, row in enumerate(table['rows']):
            percentile = table['percentiles'][row_idx]
            classification = get_class(percentile)
            score = row[cat_idx]
            
            min_score = score
            max_score = score
            if row_idx == 0:
                max_score = 999
            elif row_idx == len(table['rows']) - 1:
                min_score = 0
            else:
                prev_score = table['rows'][row_idx - 1][cat_idx]
                max_score = prev_score - 1
            
            if min_score > max_score:
                min_score = score
                max_score = score

            sql = f"INSERT INTO psychological_norms (test_name, table_name, demographic_type, demographic_value, min_score, max_score, percentile, classification) VALUES ('{table['test_name']}', '{table['table_name']}', '{table['demographic_type']}', '{category}', {min_score}, {max_score}, {percentile}, '{classification}');"
            sql_statements.append(sql)

